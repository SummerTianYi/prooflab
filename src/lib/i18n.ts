export const messages: Record<Locale, MessageCatalog> = {
  en,
  "zh-CN": zhCN,
  "zh-TW": zhTW,
  ja,
  es,
  fr,
  de,
  "pt-BR": ptBR,
  ko,
};

function matchLocale(value: string): Locale | null {
  const normalized = value.trim().replaceAll("_", "-").toLowerCase();

  if (!normalized) return null;
  if (
    normalized === "zh-tw" ||
    normalized.startsWith("zh-tw-") ||
    normalized === "zh-hk" ||
    normalized.startsWith("zh-hk-") ||
    normalized === "zh-mo" ||
    normalized.startsWith("zh-mo-") ||
    normalized.startsWith("zh-hant")
  ) {
    return "zh-TW";
  }
  if (normalized === "zh" || normalized.startsWith("zh-")) return "zh-CN";
  if (normalized === "pt-br" || normalized.startsWith("pt-")) return "pt-BR";

  const language = normalized.split("-")[0];
  const simpleMatches: Record<string, Locale> = {
    en: "en",
    ja: "ja",
    es: "es",
    fr: "fr",
    de: "de",
    pt: "pt-BR",
    ko: "ko",
  };
  return simpleMatches[language] ?? null;
}

export function resolvePreferredLocale(preferences: readonly string[]): Locale {
  for (const preference of preferences) {
    const locale = matchLocale(preference);
    if (locale) return locale;
  }
  return DEFAULT_LOCALE;
}

export function isLocale(value: string | null | undefined): value is Locale {
  return value !== null && value !== undefined && localeOptions.some(
    (option) => option.code === value,
  );
}

export function translate(
  locale: Locale,
  key: MessageKey,
  values: Record<string, string | number> = {},
): string {
  return messages[locale][key].replace(/\{([A-Za-z][A-Za-z0-9]*)\}/g, (match, name) =>
    Object.hasOwn(values, name) ? String(values[name]) : match,
  );
}
